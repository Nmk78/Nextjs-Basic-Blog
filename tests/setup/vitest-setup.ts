Object.defineProperty(globalThis, 'crypto', {
  value: {
    ...globalThis.crypto,
    getRandomValues: (array: Uint8Array) => {
      for (let i = 0; i < array.length; i++) {
        array[i] = Math.floor(Math.random() * 256);
      }
      return array;
    },
  },
});
